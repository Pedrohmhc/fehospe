    <h2 class="news-title">União dos hospitais favorece o debate, diz presidente da Fehospe</h2>

    <div class="news-meta">
      <p>Publicado em: 15 de abril de 2025 | Categoria: Eventos</p>
    </div>

    <div class="news-content">
      <p>A Federação das Misericórdias e Entidades Filantrópicas de Pernambuco (Fehospe) está mobilizando todos seus associados para participarem do 28º Congresso Nacional das Santas Casas e Hospitais Filantrópicos, que acontecerá em Brasília, nos dias 15 e 16 de agosto. “Entendemos que o Congresso é de muita valia para que possamos avançar nas questões referentes ao financiamento do sistema, a melhoria das condições das instituições, tendo em vista as dificuldades que estamos vivendo. Temos convicção de que a junção de pessoas, a unidade do grupo, fortalece nossos pleitos e faz com que tenhamos êxito nas demandas”, afirmou a presidente da Fehospe, Tereza de Jesus Campos Neta.</p>



      <p>Em seu primeiro mandato à frente da Fehospe, Teresa ressaltou que o papel das Federações é de suma importância para alimentar os hospitais de informação, reforçar o relacionamento com o Ministério da Saúde, apresentar as bandeiras que estão sendo defendidas em âmbito nacional pela CMB e, ao mesmo tempo, trazer os associados para caminharem juntos. “Entendemos que assim vamos construir um bom debate, levando à CMB, que representa bem todos os hospitais, os pleitos e desejos de nosso segmento em cada estado. Nossa representatividade busca fazer a defesa dos interesses inerentes aos hospitais, que vão dar, como resposta, um atendimento de qualidade às pessoas, oferecendo também um ambiente favorável para o trabalho de seus profissionais”.</p>
      
      
      
      <p>Tereza reforçou, ainda, que o Congresso traz uma oportunidade para buscar no Ministério da Saúde respostas às questões pendentes, além de proporcionar um momento de conhecimento e troca de experiências com os demais participantes. “Esses encontros e momentos de congratulações dos hospitais são muito importantes. Primeiro, para que possamos aprender e ter um intercâmbio de conhecimento. A partir da experiência do outro, podemos aplicar em nossos hospitais, inovar e, assim, evoluir e ter conquistas”, garantiu.</p>
      
      
      
      <p>Para convidar os parlamentares da bancada Federal, a Fehospe conta com o envolvimento dos hospitais, para que deputados e senadores estejam presentes ao evento. “Em Pernambuco temos uma bancada de deputados bem engajados e preocupados com a questão dos hospitais filantrópicos, até porque eles são preocupados com o SUS e nós fazemos parte dele. No ano passado, tivemos uma participação importante, e esperamos, nesse ano, ter êxito nessa articulação”, disse Tereza.</p>
      
      
      
      <p>Com a participação dos presidenciáveis na programação científica do Congresso, Teresa disse que é preciso que os candidatos estejam comprometidos com a Saúde e a população. “Temos que ter candidatos comprometidos com um sistema de saúde que funciona. Como os hospitais filantrópicos e santas casas atendem hoje, na maioria dos estados, mais de 50% do SUS, chegando a até 90% em alguns procedimentos, esperamos que os candidatos estejam sensíveis aos problemas e levantem essa bandeira, que não é exclusiva do nosso segmento, mas é da população, pois ela precisa deste sistema e tem sido atendida com qualidade”.</p>
      
      
      
