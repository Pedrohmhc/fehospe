    <h2 class="news-title">Fehospe recebe representante do Ministério da Saúde</h2>

    <div class="news-meta">
      <p>Publicado em: 15 de abril de 2025 | Categoria: Eventos</p>
    </div>

    <div class="news-content">
      <p>A presidente da Federação dos Hospitais Filantrópicos de Pernambuco (Fehospe) e superintendente-geral do IMIP, Tereza Campos Neta, recebeu, no dia 9 de agosto, a visita do secretário de Atenção à Saúde, do Ministério da Saúde, Francisco de Assis Figueiredo. Na ocasião, o gestor conheceu a estrutura do IMIP, que é a maior instituição filantrópica 100% SUS do Brasil.</p>



      <p>O representante do Secretário Estadual de Saúde, Humberto Antunes, e diretores do IMIP também acompanharam a visita. Francisco de Assis visitou os serviços de nefrologia, oncologia, cardiologia e saúde da mulher, além de conhecer a sala de Defesa de Tese.</p>
      
      
      
      <p>O IMIP conta com 1075 leitos e, por dia, circulam em média 20 mil pessoas pelo seu complexo hospitalar. Referência na atenção materno-infantil, o Instituto realiza, em média, 600 partos por mês.</p>
      
      
      
      <p>Para dar conta de toda essa estrutura, são mais de 5 mil funcionários que trabalham diariamente para garantir uma atenção de qualidade.</p>
      
      
      
