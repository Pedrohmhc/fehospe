    <div class="news-content">
      <p>Na manhã da última quarta-feira, 26 de março de 2025, a Confederação das Santas Casas e Hospitais Filantrópicos (CMB) participou de sua primeira reunião com o novo ministro da Saúde, Alexandre Padilha. O encontro foi uma oportunidade estratégica para apresentar e reforçar pautas prioritárias da rede filantrópica, essencial para o atendimento pelo Sistema Único de Saúde (SUS) em todo o país.</p>



      <p>Estiveram presentes na reunião, Mírocles Véras, Presidente da Confederação das Santas Casas e Hospitais Filantrópicos (CMB); Flaviano Feu Venturim, vice-presidente da CMB; Deputado Federal Antônio Brito, presidente da Frente Parlamentar de Apoio às Santas Casas; Tereza Campos, presidente do Conselho Consultivo da CMB e Presidente do Conselho Administrativo da Federação das Misericórdias e Entidades Filantrópicas de Pernambuco (FEHOSPE); Vinicius Linhares, presidente da FEMICE; Mário Cesar Homsi Bernardes, diretor-geral da CMB; Monaliza Costa Santos, diretora jurídica e administrativa da CMB; Hermes Sampaio, assessor parlamentar do deputado Antônio Brito. Pelo Ministério da Saúde, também estiveram presentes, Morzat Julio Tabosa Sales, secretário de Atenção Especializada à Saúde; Nilton Pereira Júnior, secretário adjunto de Atenção Especializada à Saúde; e Carlos Amílcar Salgado, diretor do Departamento de Regulação Assistencial e Controle (DRAC).</p>
      
      
      
      <p>Durante a reunião, foram debatidos temas como a regulamentação do Grupo de Trabalho entre a CMB, CONASS, CONASEMS e Ministério da Saúde; a liberação dos recursos da Lei nº 14.820/2024 e a participação do Ministério da Saúde no 33° Congresso da CMB, que será realizado entre os dias 12 e 14 de agosto de 2025, em Brasília.</p>
      
