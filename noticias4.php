    <div class="news-content">
      <p>Federação de Hospitais Filantrópicos de Pernambuco (FEHOSPE) se reuniu com o governador Paulo Câmara (PSB), e com o secretário estadual de Saúde, André Longo.</p>



      <p>A superintendente-geral do IMIP, Tereza Campos, participou na tarde desta segunda-feira (25), no Palácio do Campo das Princesas, da reunião da Federação de Hospitais Filantrópicos de Pernambuco (FEHOSPE) com o governador Paulo Câmara (PSB), e com o secretário estadual de Saúde, André Longo.</p>
      
      
      
      <p>Na pauta solicitada pela Federação presidida pela gestora, assuntos relacionados aos hospitais filantrópicos do Estado, como a regularização do repasse mensal, o cronograma de repasse do passivo do tesouro com essas unidades, a reposição de ambulâncias para as Unidades dos contratos de gestão, portal da transparência e a sustentabilidade das instituições filantrópicas. Participaram ainda do encontro Gil Brasileiro (Hospital Tricentenário), Filipe Bitu (Hospital do Câncer) e José Adelino dos Santos Neto (secretário-executivo da SES).</p>
      
      
      
      <p>O governador assumiu o compromisso de repor as ambulâncias das unidades de OS até junho desse ano, além de ter marcado um novo encontro para abril com intuito de informar o cronograma de repasse do passivo. “A reunião foi positiva. O governador se comprometeu a realizar uma agenda permanente com a Federação, reconhecendo a importância de manter a sustentabilidade dessas instituições, tendo em vista importância das mesmas para o SUS”, afirmou a gestora.</p>
      
      
      
      <p>Veja a notícia na Folha de Pernambuco: https://www.folhape.com.br/politica/politica/pernambuco/2019/03/27/BLG,9960,7,1168,POLITICA,2419-GESTORES-HOSPITAIS-FILANTROPICOS-REUNEM-COM-GOVERNADOR.aspx </p>
